package com.bank.diwa0.dao;

public class CodiceQuattro {

	private Long codice;
	private Long valoKeyNum;
	private String valoKeyTxt;

	public Long getCodice() {
		return codice;
	}

	public void setCodice(Long codice) {
		this.codice = codice;
	}

	public Long getValoKeyNum() {
		return valoKeyNum;
	}

	public void setValoKeyNum(Long valoKeyNum) {
		this.valoKeyNum = valoKeyNum;
	}

	public String getValoKeyTxt() {
		return valoKeyTxt;
	}

	public void setValoKeyTxt(String valoKeyTxt) {
		this.valoKeyTxt = valoKeyTxt;
	}
}